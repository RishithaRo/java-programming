import java.util.*;
public class Assignop
{
    public static void main(String args[])
    {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        a+=5;
        System.out.println(a);
        a-=5;
        System.out.println(a);
        a*=5;
        System.out.println(a);
        a/=5;
        System.out.println(a);
        a%=2;
        System.out.println(a);
    }
}
